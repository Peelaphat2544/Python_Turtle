import turtle
turtle.bgcolor("black")
T = turtle.Pen()
T.shape('turtle')


def Go (x,y):
    T.penup()
    T.goto(x,y)
    T.pendown()

def Bull1 ():
    T.pen(pencolor="green", fillcolor="green", pensize=1, speed=9)
    T.begin_fill()
    for i in range (6):
        T.fd(20)
        T.left(90)
    T.end_fill()
    T.rt(180)
    T.penup()
    T.fd(2)
    T.pendown()
def Bear ():
    T.pen(pencolor="red", fillcolor="red", pensize=1, speed=9)
    T.begin_fill()
    T.rt(90)
    T.fd(50)
    T.lt(90)
    T.fd(20)
    T.lt(90)
    T.fd(50)
    T.end_fill()
    T.rt(180)
    T.fd(50)
    T.lt(90)
    T.penup()
    T.fd(2)
    T.pendown
